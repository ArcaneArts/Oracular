  static String _projectStructure(SetupConfig config) {
    final StringBuffer buf = StringBuffer();
    buf.writeln('# Project Structure');
    buf.writeln();
    buf.writeln('```text');
    buf.writeln('${p.basename(config.outputDir)}/');
    buf.writeln('|-- ${SetupGuidance.mainProjectName(config)}/'
        '              # ${SetupGuidance.mainProjectLabel(config)}');
    if (config.createModels) {
      buf.writeln('|-- ${config.modelsPackageName}/'
          '              # Shared models package');
    }
    if (config.createServer) {
      buf.writeln('|-- ${config.serverPackageName}/'
          '              # Dart REST API server');
    }
    buf.writeln('|-- config/                              # Setup metadata + Firebase JSON');
    buf.writeln('|-- references/                          # Vendored Arcane reference docs');
    buf.writeln('|-- docs/                                # This documentation folder');
    buf.writeln('|-- GET_STARTED.md                       # Auto-generated quick-start guide');
    buf.writeln('|-- README.md');
    buf.writeln('`-- ...');
    buf.writeln('```');
    buf.writeln();

    if (config.template.isFlutterApp) {
      buf.writeln('## Inside `${config.appName}/`');
      buf.writeln();
      buf.writeln('```text');
      buf.writeln('lib/');
      buf.writeln('|-- main.dart                        # App entrypoint');
      if (config.template == TemplateType.arcaneBeamer) {
        buf.writeln('|-- routes/                          # Beamer location definitions');
        buf.writeln('|-- screens/                         # Top-level screens');
      }
      if (config.template == TemplateType.arcaneDock) {
        buf.writeln('|-- tray/                            # Tray icon + menu');
        buf.writeln('|-- windows/                         # Mini-window components');
      }
      buf.writeln('|-- components/                      # Reusable widgets');
      buf.writeln('|-- services/                        # Domain logic & data access');
      buf.writeln('|-- utils/                           # Helpers, constants');
      if (config.useFirebase) {
        buf.writeln('`-- firebase_options.dart            # FlutterFire-generated config');
      }
      buf.writeln('```');
      buf.writeln();
    } else if (config.template.isJasprApp) {
      buf.writeln('## Inside `${config.webPackageName}/`');
      buf.writeln();
      buf.writeln('```text');
      buf.writeln('lib/');
      buf.writeln('|-- main.client.dart                # Client-side entrypoint');
      buf.writeln('|-- app.dart                        # Top-level <App/> component');
      buf.writeln('|-- routes/');
      buf.writeln('|   `-- app_router.dart             # Route table');
      buf.writeln('|-- screens/                        # Page-level components');
      buf.writeln('|-- components/                     # Reusable UI components');
      buf.writeln('`-- utils/');
      buf.writeln('    `-- constants.dart              # Route/app constants');
      buf.writeln('web/');
      buf.writeln('|-- index.html                      # HTML shell + Firebase JS hooks');
      buf.writeln('|-- styles.css                      # Global theme + reset');
      buf.writeln('`-- assets/');
      buf.writeln('```');
      buf.writeln();
    } else if (config.template.isDartCli) {
      buf.writeln('## Inside `${config.appName}/`');
      buf.writeln();
      buf.writeln('```text');
      buf.writeln('bin/');
      buf.writeln('`-- main.dart                       # CLI entrypoint');
      buf.writeln('lib/');
      buf.writeln('|-- cli/');
      buf.writeln('|   |-- commands.dart               # Command tree definition');
      buf.writeln('|   `-- handlers/                   # Implementation per command');
      buf.writeln('`-- services/                       # Reusable domain logic');
      buf.writeln('```');
      buf.writeln();
    }

    if (config.createServer) {
      buf.writeln('## Inside `${config.serverPackageName}/`');
      buf.writeln();
      buf.writeln('```text');
      buf.writeln('bin/');
      buf.writeln('|-- main.dart                       # Cloud Run / shelf entrypoint');
      buf.writeln('lib/');
      buf.writeln('|-- routes/                         # HTTP route handlers');
      buf.writeln('|-- middleware/                     # Auth, CORS, logging');
      buf.writeln('`-- services/                       # Domain layer');
      buf.writeln('Dockerfile                          # Container build');
      buf.writeln('script_deploy.sh                    # gcloud run deploy helper');
      buf.writeln('```');
      buf.writeln();
    }

    if (config.createModels) {
      buf.writeln('## Inside `${config.modelsPackageName}/`');
      buf.writeln();
      buf.writeln(
        'Pure-Dart package shared between the app and (optionally) the '
        'server. Add `@artifact` types here so JSON serialization stays '
        'consistent across tiers.',
      );
      buf.writeln();
    }
    return buf.toString();
  }

  static String _development(SetupConfig config) {
    final StringBuffer buf = StringBuffer();
    buf.writeln('# Development Workflow');
    buf.writeln();
    buf.writeln('## Hot reload / hot restart');
    buf.writeln();
    if (config.template.isFlutterApp) {
      buf.writeln(
        'While `flutter run` is attached, press `r` for hot reload, `R` for '
        'hot restart, `o` to switch the target platform, `q` to quit. Run '
        '`flutter run -d chrome` to launch the web build.',
      );
    } else if (config.template.isJasprApp) {
      buf.writeln(
        '`jaspr serve` watches the entire project for changes and rebuilds '
        'on save. To restart manually: `oracular scripts exec killall && '
        'oracular scripts exec serve`.',
      );
    } else {
      buf.writeln(
        'CLI apps re-run on each invocation; for tight loops use '
        '`dart run --enable-vm-service bin/main.dart` and connect a debugger.',
      );
    }
    buf.writeln();

    buf.writeln('## Code generation');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('cd ${SetupGuidance.mainProjectName(config)}');
    buf.writeln('dart run build_runner build --delete-conflicting-outputs');
    buf.writeln('# or, while editing models continuously:');
    buf.writeln('dart run build_runner watch --delete-conflicting-outputs');
    buf.writeln('```');
    buf.writeln();

    if (config.createModels) {
      buf.writeln('Run the same commands inside `${config.modelsPackageName}/` '
          'when you change `@artifact` annotated classes.');
      buf.writeln();
    }

    buf.writeln('## Adding a screen / route');
    buf.writeln();
    if (config.template == TemplateType.arcaneTemplate) {
      buf.writeln(
        '1. Create a new widget under `lib/components/` (or `lib/screens/`).\n'
        '2. Push it from a button: `Navigator.of(context).push(MaterialPageRoute(...))`.\n'
        '3. Or - swap to the Beamer template later for declarative routing.',
      );
    } else if (config.template == TemplateType.arcaneBeamer) {
      buf.writeln(
        '1. Define a new `BeamLocation` in `lib/routes/`.\n'
        '2. Add it to the router list in `lib/main.dart`.\n'
        '3. Navigate with `context.beamToNamed(\'/your-route\')`.',
      );
    } else if (config.template.isJasprApp) {
      buf.writeln(
        '1. Add a route constant in `lib/utils/constants.dart`.\n'
        '2. Create the screen widget under `lib/screens/`.\n'
        '3. Wire the route case in `lib/routes/app_router.dart`.\n'
        '4. Link to it via `Button.primary(href: AppRoutes.your, label: ...)`.',
      );
    }
    buf.writeln();

    buf.writeln('## Linting & formatting');
    buf.writeln();
    buf.writeln('```bash');
    if (config.template.isFlutterApp) {
      buf.writeln('flutter analyze');
      buf.writeln('dart format .');
    } else {
      buf.writeln('dart analyze');
      buf.writeln('dart format .');
    }
    buf.writeln('```');
    buf.writeln();

    buf.writeln('## Testing');
    buf.writeln();
    buf.writeln('```bash');
    if (config.template.isFlutterApp) {
      buf.writeln('flutter test');
    } else {
      buf.writeln('dart test');
    }
    buf.writeln('```');
    return buf.toString();
  }

  static String _firebase(SetupConfig config) {
    final String projectId = config.firebaseProjectId ?? 'YOUR_PROJECT_ID';
    final StringBuffer buf = StringBuffer();
    buf.writeln('# Firebase Setup');
    buf.writeln();
    buf.writeln('## Project: `$projectId`');
    buf.writeln();
    buf.writeln('Useful consoles:');
    buf.writeln();
    buf.writeln(
      '- Overview: ${SetupGuidance.firebaseOverviewUrl(projectId)}\n'
      '- Authentication: ${SetupGuidance.firebaseAuthenticationConsoleUrl(projectId)}\n'
      '- Firestore: ${SetupGuidance.firebaseFirestoreConsoleUrl(projectId)}\n'
      '- Storage: ${SetupGuidance.firebaseStorageConsoleUrl(projectId)}\n'
      '- Hosting: ${SetupGuidance.firebaseHostingConsoleUrl(projectId)}',
    );
    buf.writeln();

    buf.writeln('## Bootstrap (run once)');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('cd ${config.outputDir}');
    buf.writeln('oracular deploy firebase-setup');
    buf.writeln('```');
    buf.writeln();
    buf.writeln(
      'This:\n'
      '- creates / reuses a Firebase web app\n'
      '- generates `firebase_options.dart` (Flutter) or `firebase_config.js` (Jaspr)\n'
      '- enables required Google Cloud APIs\n'
      '- writes `firebase.json`, `.firebaserc`, `firestore.rules`, `storage.rules`',
    );
    buf.writeln();

    buf.writeln('## Manual checklist');
    buf.writeln();
    buf.writeln(
      '1. **Authentication:** Open the auth console and enable the providers '
      'you need (Google / Email-Password / Anonymous).\n'
      '2. **Firestore:** Create the database (Native mode) the first time you '
      'visit the console. Pick a region close to your users.\n'
      '3. **Storage:** Click "Get started" if your app uploads files.\n'
      '4. **Indexes / rules:** Edit `firestore.rules`, `firestore.indexes.json`, '
      '`storage.rules` then `oracular deploy firestore` / `oracular deploy storage`.',
    );
    buf.writeln();

    buf.writeln('## Regenerate config');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('oracular deploy generate-configs');
    buf.writeln('```');

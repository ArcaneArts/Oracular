  static String _troubleshooting(SetupConfig config) {
    final StringBuffer buf = StringBuffer();
    buf.writeln('# Troubleshooting');
    buf.writeln();

    buf.writeln('## `pub get` complains about jaspr / arcane_auth_jaspr');
    buf.writeln();
    buf.writeln(
      'The base Jaspr template uses `jaspr ^0.23.0`. There is no published '
      '`arcane_auth_jaspr` that supports `0.23.x` yet, so do not add it. If '
      'an old project pulls it in, remove the dependency line and re-run '
      '`dart pub get`.',
    );
    buf.writeln();

    buf.writeln('## `pub get` complains about Flutter SDK in Jaspr');
    buf.writeln();
    buf.writeln(
      'Oracular vendors a pure-Dart `jpatch` shim into `.oracular_deps/` and '
      'adds a `dependency_overrides` block to your `pubspec.yaml` whenever a '
      'Jaspr / pure-Dart package depends on `arcane_models`. If you delete '
      'that folder, re-run the wizard or copy `.oracular_deps/jpatch` from a '
      'fresh project.',
    );
    buf.writeln();

    buf.writeln('## FlutterFire / Firebase config keeps failing');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('oracular check firebase');
    buf.writeln('firebase login --reauth');
    buf.writeln('oracular deploy firebase-setup');
    buf.writeln('```');
    buf.writeln();
    buf.writeln(
      'Pre-flight checks: you must be the project Owner, Firebase tools must '
      'be `>= 13.x`, and the Firebase project must already exist (Oracular '
      'will not create the project itself).',
    );
    buf.writeln();

    buf.writeln('## Hot reload not picking up changes');
    buf.writeln();
    if (config.template.isFlutterApp) {
      buf.writeln(
        'Hot reload skips changes to `main()`, top-level state, and code-gen '
        'output. Press `R` for a hot **restart** instead. If you edited '
        '`@artifact` types, run `build_runner build` first.',
      );
    } else if (config.template.isJasprApp) {
      buf.writeln(
        '`jaspr serve` watches the project but builders are slower. Run '
        '`oracular scripts exec build_runner` once after large edits, then '
        '`oracular scripts exec serve`.',
      );
    } else {
      buf.writeln(
        'CLI apps do not hot reload - re-run the entrypoint after each edit.',
      );
    }
    buf.writeln();

    if (config.createServer) {
      buf.writeln('## Cloud Run: 401 / 403');
      buf.writeln();
      buf.writeln(
        'Verify `service-account.json` is the Firebase Admin key (not a generic '
        'GCP service account) and that it has the **Firebase Admin SDK** role. '
        'Re-deploy with `./script_deploy.sh` after replacing the key.',
      );
      buf.writeln();
    }

    buf.writeln('## Rebuilding the docs/ folder');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('oracular guide');
    buf.writeln('```');
    return buf.toString();
  }


/// Generates a `docs/` folder inside a created Oracular project containing
/// quick-start, command, deployment and troubleshooting how-tos. The folder
/// always contains an `index.md` with a table of contents and a permutation-
/// aware set of pages tailored to the chosen template / Firebase / server
/// flags.
class DocsGenerator {
  /// Path to the docs folder that lives at the root of the user project.
  static String docsDir(SetupConfig config) {
    return p.join(config.outputDir, 'docs');
  }

  /// Write a complete `docs/` folder. Existing files are overwritten so the
  /// guide always reflects the current configuration.
  static Future<Directory> write(SetupConfig config) async {
    final Directory dir = Directory(docsDir(config));
    if (!dir.existsSync()) {
      await dir.create(recursive: true);
    }

    final List<_DocPage> pages = _buildPages(config);

    for (final _DocPage page in pages) {
      final File f = File(p.join(dir.path, page.filename));
      await f.writeAsString(page.content);
      verbose('Wrote ${page.filename}');
    }

    final File index = File(p.join(dir.path, 'README.md'));
    await index.writeAsString(_buildIndex(config, pages));

    return dir;
  }

  static List<_DocPage> _buildPages(SetupConfig config) {
    final List<_DocPage> pages = <_DocPage>[
      _DocPage(
        filename: '01-getting-started.md',
        title: 'Getting Started',
        content: _gettingStarted(config),
      ),
      _DocPage(
        filename: '02-commands.md',
        title: 'Command Reference',
        content: _commands(config),
      ),
      _DocPage(
        filename: '03-project-structure.md',
        title: 'Project Structure',
        content: _projectStructure(config),
      ),
      _DocPage(
        filename: '04-development.md',
        title: 'Development Workflow',
        content: _development(config),
      ),
    ];

    if (config.useFirebase) {
      pages.add(
        _DocPage(
          filename: '05-firebase.md',
          title: 'Firebase Setup',
          content: _firebase(config),
        ),
      );
      pages.add(
        _DocPage(
          filename: '06-deployment.md',
          title: 'Deployment',
          content: _deployment(config),
        ),
      );
    } else {
      pages.add(
        _DocPage(
          filename: '05-firebase.md',
          title: 'Firebase (Enable Later)',
          content: _firebaseLater(config),
        ),
      );
    }

    if (config.createServer) {
      pages.add(
        _DocPage(
          filename: '07-server.md',
          title: 'Server Deployment',
          content: _server(config),
        ),
      );
    }

    if (config.createModels) {
      pages.add(
        _DocPage(
          filename: '08-models.md',
          title: 'Shared Models',
          content: _models(config),
        ),
      );
    }

    pages.add(
      _DocPage(
        filename: '09-troubleshooting.md',
        title: 'Troubleshooting',
        content: _troubleshooting(config),
      ),
    );

    pages.add(
      _DocPage(
        filename: '10-resources.md',
        title: 'Further Reading',
        content: _resources(config),
      ),
    );

    return pages;
  }

  static String _buildIndex(SetupConfig config, List<_DocPage> pages) {
    final StringBuffer buf = StringBuffer();
    buf.writeln('# ${config.baseClassName} Docs');
    buf.writeln();
    buf.writeln(
      'Generated by [Oracular](https://pub.dev/packages/oracular) v$oracularVersion'
      ' on ${DateTime.now().toIso8601String().substring(0, 10)}.',
    );
    buf.writeln();
    buf.writeln('## Project at a Glance');
    buf.writeln();
    buf.writeln('| Setting | Value |');
    buf.writeln('| --- | --- |');
    config.toDisplayMap().forEach((String k, String v) {
      buf.writeln('| $k | $v |');
    });
    buf.writeln();
    buf.writeln('## Contents');
    buf.writeln();
    for (int i = 0; i < pages.length; i++) {
      final _DocPage page = pages[i];
      buf.writeln('${i + 1}. [${page.title}](./${page.filename})');
    }
    buf.writeln();
    buf.writeln('## Quick Reference');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('# Run the project');
    buf.writeln('cd ${SetupGuidance.mainProjectName(config)}');
    buf.writeln(SetupGuidance.runCommand(config));
    buf.writeln();
    buf.writeln('# Re-open this guide');
    buf.writeln('oracular guide');
    buf.writeln('oracular open guide');
    buf.writeln('```');
    return buf.toString();
  }


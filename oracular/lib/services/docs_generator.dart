  static String _gettingStarted(SetupConfig config) {
    final String mainPath = SetupGuidance.mainProjectName(config);
    final String runCmd = SetupGuidance.runCommand(config);
    final StringBuffer buf = StringBuffer();
    buf.writeln('# Getting Started');
    buf.writeln();
    buf.writeln(
      'This guide gets ${config.baseClassName} running on your machine.',
    );
    buf.writeln();
    buf.writeln('## 1. Verify your environment');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('oracular check tools');
    if (config.useFirebase) {
      buf.writeln('oracular check firebase');
    }
    buf.writeln('```');
    buf.writeln();
    buf.writeln('## 2. Install dependencies');
    buf.writeln();
    buf.writeln(
      'Oracular already ran `${_pubGetCommand(config)}` for every package, '
      'but if you ever need to redo it manually:',
    );
    buf.writeln();
    buf.writeln('```bash');
    if (config.template.isFlutterApp) {
      buf.writeln('cd $mainPath && flutter pub get');
    } else {
      buf.writeln('cd $mainPath && dart pub get');
    }
    if (config.createModels) {
      buf.writeln('cd ../${config.modelsPackageName} && dart pub get');
    }
    if (config.createServer) {
      buf.writeln('cd ../${config.serverPackageName} && dart pub get');
    }
    buf.writeln('```');
    buf.writeln();
    buf.writeln('## 3. Run the app');
    buf.writeln();
    buf.writeln('```bash');
    buf.writeln('cd $mainPath');
    buf.writeln(runCmd);
    buf.writeln('```');
    buf.writeln();
    if (config.template.isFlutterApp) {
      buf.writeln(
        '`flutter run` opens an interactive console. Press `r` to hot-reload, '
        '`R` to hot-restart, and `q` to quit.',
      );
    } else if (config.template.isJasprApp) {
      buf.writeln(
        'Jaspr serves at <http://localhost:8080> by default with hot reload. '
        'Pass `--port 9000` to change ports or `--release` for an optimized build.',
      );
    } else {
      buf.writeln(
        'Pass `--help` (or any other flag) to see the available CLI commands.',
      );
    }
    buf.writeln();
    if (config.useFirebase) {
      buf.writeln('## 4. Wire up Firebase');
      buf.writeln();
      buf.writeln('```bash');
      buf.writeln('cd ${config.outputDir}');
      buf.writeln('oracular deploy firebase-setup');
      buf.writeln('```');
      buf.writeln();
      buf.writeln(
        'See [Firebase Setup](./05-firebase.md) for the full step-by-step.',
      );
      buf.writeln();
    }
    if (config.createServer) {
      buf.writeln('## 5. Run the server locally');
      buf.writeln();
      buf.writeln('```bash');
      buf.writeln('cd ${config.serverPackageName}');
      buf.writeln('dart run bin/main.dart');
      buf.writeln('```');
      buf.writeln();
      buf.writeln(
        'See [Server Deployment](./07-server.md) for production deploys.',
      );
      buf.writeln();
    }
    return buf.toString();
  }

